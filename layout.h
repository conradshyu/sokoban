/*
 * layout.h
 * Definition of Sokoban games
 *
 * Remarks from Wikipedia
 * Sokoban (warehouse keeper) is a type of transport puzzle, in which the
 * player pushes boxes or crates around in a warehouse, trying to get them
 * to storage location. The puzzle is usually implemented as a video game.
 * Sokoban was created in 1981 by Hiroyuki Imabayashi, and published in
 * 1982 by Thinking Rabbit, a software house based in Takarazuka, Japan.
 *
 * Only one box can be pushed at a time.
 * A box cannot be pulled.
 * The player cannot walk through boxes or walls.
 * The puzzle is solved when all boxes are located at storage locations.
 *
 * Sokoban can be studied using the theory of computational complexity.
 * The problem of using Sokoban puzzles has been proved to be NP-hard. This
 * is alos interesting for artificial intelligence researchers, because
 * solving Sokoban can be compared to designing a robot which moves boxes
 * in a warehouse. Further work has shown that solving Sokoban problems is
 * also PSPACE-complete. Sokoban is difficult not only due to its branching
 * factor (which is comparable to chess), but also its enormous search tree
 * depth; some levels require more than 1000 "pushes". Skilled human players
 * rely mostly on heuristics; they are usually able to quickly discard futile
 * or redundant lines of play, and recognize patterns and subgoals, drastically
 * cutting down on the amount of search.
 *
 *  : black background
 * .: blue background
 * #: wall
 * *: ball
 * @: avatar
 * $: empty box
 * &: box with ball
*/

